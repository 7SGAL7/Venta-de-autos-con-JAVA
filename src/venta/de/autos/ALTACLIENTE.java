    private void jToggleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton2ActionPerformed
        try {
            if((jTextField1.getText().length()==0)||(jTextField2.getText().length()==0)||(jTextField3.getText().length()==0)||(jTextField4.getText().length()==0)){
                JOptionPane.showMessageDialog(null,"uno o mas campos vacios");  
            }else{
                File file = new File(Foto);
                fi = new FileInputStream(file);
                PreparedStatement pst = cn.prepareStatement("INSERT INTO cliente(Nombre,Apellido,Telefono,Ingreso,foto) VALUES(?,?,?,?,?)");
                pst.setString(1,jTextField1.getText());
                pst.setString(2,jTextField2.getText());
                pst.setString(3,jTextField3.getText());
                pst.setString(4,"$" + jTextField4.getText());
                pst.setBinaryStream(5,fi);
               
                int a = pst.executeUpdate();
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                if(a>0){
                    JOptionPane.showMessageDialog(null,"Registro exitoso"); 
                }
                else{
                    JOptionPane.showMessageDialog(null,"Error al agregar");
                }
            }
        }catch(HeadlessException | SQLException e){
        
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ALTACLIENTE.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }//GEN-LAST:event_jToggleButton2ActionPerformed

