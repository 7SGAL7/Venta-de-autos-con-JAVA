    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       try{
           if((jTextField1.getText().length()==0)||(jTextField2.getText().length()==0)){
               JOptionPane.showMessageDialog(null,"uno o mas campos vacios");
           }else{
                File file = new File(Foto);
                fi = new FileInputStream(file);
                PreparedStatement pst = cn.prepareStatement("INSERT INTO auto(Modelo,Marca,Precio,foto) VALUES(?,?,?,?)");
                pst.setString(1,jTextField1.getText());
                pst.setString(2,(String)jComboBox1.getSelectedItem());
                pst.setString(3,"$"+jTextField2.getText());
                pst.setBinaryStream(4,fi);
                pst.executeUpdate(); 
                jTextField1.setText("");
                jTextField2.setText("");
                JOptionPane.showMessageDialog(null, "ingreso exitos");
           }  
        }catch (SQLException ex) {
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ALTAAUTO.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

